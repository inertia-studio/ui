/**
 * Render an icon by name. Uses built-in SVG icons for common admin panel icons.
 */
export function Icon({ name, size = 'md', className }: IconProps) {
    const IconComponent = builtinIcons[name];

    if (!IconComponent) {
        // Fallback: render a placeholder square for unknown icons
        return <span className={cn(sizeClasses[size], 'inline-block rounded bg-s-surface-alt', className)} />;
    }

    return <IconComponent className={cn(sizeClasses[size], className)} />;
}

/**
 * Render an icon from an IconSchema object (as received from the backend).
 */
export function SchemaIcon({ schema, size, className }: { schema: IconSchema | null; size?: IconProps['size']; className?: string }) {
    if (!schema) return null;

    return (
        <Icon
            name={schema.name}
            provider={schema.provider ?? undefined}
            variant={schema.variant ?? undefined}
            size={size}
            className={className}
        />
    );
}
